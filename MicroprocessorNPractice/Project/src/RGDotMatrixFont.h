unsigned long long toSimpleFont(char ch) {
    for (int i = 0; i < NUM_OF_SIMPLE_FONT; i++) {
        if (ch == SIMPLE_FONT_CHARACTERS[i]) {
            return SIMPLE_FONTS[i];
        }
    }
    return FONT_EMPTY;
}

